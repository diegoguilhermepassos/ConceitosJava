package calculator;

public class Calculadora {
	
	public Calculadora () {
		
	}
	
	public double somar (double x, double y) {
		double dados = x + y;
		return dados;
	}
	
	public double subtrair (double x, double y) {
		double dados = x - y;
		return dados;
	}
	
	public double multiplicar (double x, double y) {
		double dados = x * y;
		return dados;
	}
	
	public double dividir (double x, double y) {
		double dados = x / y;
		return dados;
	}
	
	
		
}


	